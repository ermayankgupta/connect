const mongoose = require("mongoose");
const { Schema } = mongoose;

const userModal = Schema(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
  }
);

const User = mongoose.model("User", userModal);

module.exports = User;
